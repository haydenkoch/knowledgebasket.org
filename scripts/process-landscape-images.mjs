#!/usr/bin/env node
/**
 * Process landscape images for responsive web use.
 *
 * 1. Place source images (JPG/PNG/WebP) in: static/images/landscapes/source/
 * 2. Run: npm run images:process
 * 3. Output: static/images/landscapes/{width}/{slug}.webp and 960/{slug}.jpg fallbacks
 * 4. Generates: src/lib/data/landscape-manifest.generated.ts
 *
 * Uses Sharp for resizing and format conversion. Serves optimal image by device via srcset.
 */

import sharp from 'sharp';
import { readdir, mkdir, writeFile } from 'fs/promises';
import { join, dirname, resolve } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(join(__dirname, '..'));
const SOURCE_DIR = join(ROOT, 'static', 'images', 'landscapes', 'source');
const OUT_BASE = join(ROOT, 'static', 'images', 'landscapes');
const WIDTHS = [320, 640, 960, 1280, 1920];
const EXTENSIONS = new Set(['.jpg', '.jpeg', '.png', '.webp']);

function getExtension(filename) {
	const i = filename.lastIndexOf('.');
	return i > 0 ? '.' + filename.slice(i + 1).toLowerCase() : '';
}

function isImageFile(filename) {
	return EXTENSIONS.has(getExtension(filename));
}

function toSlug(filename) {
	const base = filename.replace(/\.[^.]+$/, '').trim();
	return base
		.toLowerCase()
		.replace(/\s+/g, '-')
		.replace(/[^a-z0-9-]/g, '-')
		.replace(/-+/g, '-')
		.replace(/^-|-$/g, '') || 'image';
}

async function ensureDir(p) {
	await mkdir(p, { recursive: true });
}

async function main() {
	let sourceFiles;
	try {
		sourceFiles = await readdir(SOURCE_DIR);
	} catch (e) {
		if (e.code === 'ENOENT') {
			console.warn('No source folder found. Create static/images/landscapes/source/ and add JPG/PNG images, then run npm run images:process');
			await writeEmptyManifest(ROOT);
			process.exit(0);
			return;
		}
		throw e;
	}

	const imageFiles = sourceFiles.filter((f) => isImageFile(f));
	if (imageFiles.length === 0) {
		console.warn(
			'No image files (.jpg, .jpeg, .png, .webp) found in source/. Files seen:',
			sourceFiles.length ? sourceFiles.join(', ') : '(none)'
		);
		await writeEmptyManifest(ROOT);
		process.exit(0);
		return;
	}

	const slugs = [];
	for (const file of imageFiles) {
		const slug = toSlug(file);
		slugs.push({ originalName: file, slug });
		const inputPath = join(SOURCE_DIR, file);
		for (const w of WIDTHS) {
			const outDir = join(OUT_BASE, String(w));
			await ensureDir(outDir);
			const pipeline = sharp(inputPath).resize(w, null, { withoutEnlargement: true });
			await pipeline.webp({ quality: 82 }).toFile(join(outDir, `${slug}.webp`));
			if (w === 960) {
				await sharp(inputPath)
					.resize(960, null, { withoutEnlargement: true })
					.jpeg({ quality: 85 })
					.toFile(join(outDir, `${slug}.jpg`));
			}
		}
		console.log('Processed:', file, '->', slug);
	}

	await writeManifest(ROOT, slugs);
	console.log('Done. Processed', imageFiles.length, 'images; widths:', WIDTHS.join(', '));
}

async function writeManifest(root, slugs) {
	const outPath = join(root, 'src', 'lib', 'data', 'landscape-manifest.generated.ts');
	const widths = JSON.stringify(WIDTHS);
	const list = slugs.map((s) => `  { originalName: ${JSON.stringify(s.originalName)}, slug: ${JSON.stringify(s.slug)} }`).join(',\n');
	const content = `/**
 * Generated by scripts/process-landscape-images.mjs — do not edit.
 * Run \`npm run images:process\` after adding images to static/images/landscapes/source/
 */
export const LANDSCAPE_WIDTHS = ${widths} as const;
export const LANDSCAPE_ENTRIES = [\n${list}\n] as const;
export const LANDSCAPE_BASE = '/images/landscapes';
`;
	await writeFile(outPath, content, 'utf8');
}

async function writeEmptyManifest(root) {
	const outPath = join(root, 'src', 'lib', 'data', 'landscape-manifest.generated.ts');
	const content = `/**
 * Generated by scripts/process-landscape-images.mjs — no processed images yet.
 * Add images to static/images/landscapes/source/ and run \`npm run images:process\`
 */
export const LANDSCAPE_WIDTHS = [320, 640, 960, 1280, 1920] as const;
export const LANDSCAPE_ENTRIES = [] as const;
export const LANDSCAPE_BASE = '/images/landscapes';
`;
	await writeFile(outPath, content, 'utf8');
}

main().catch((err) => {
	console.error(err);
	process.exit(1);
});
